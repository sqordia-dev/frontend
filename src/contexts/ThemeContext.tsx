export function ThemeProvider({ children }: { children: ReactNode }) {
  const [theme, setTheme] = useState<Theme>(() => {
    const saved = localStorage.getItem('theme');
    return (saved as Theme) || 'light';
  });

  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem('language');
    return (saved as Language) || 'en';
  });

  const [themeColorId, setThemeColorId] = useState<string>(() => {
    const saved = localStorage.getItem('themeColor');
    return saved || 'orange';
  });

  const [themeColor, setThemeColorState] = useState<ThemeColor>(() => {
    return settingsService.getThemeColorConfig(themeColorId);
  });

  // Load theme color from backend on mount
  useEffect(() => {
    const loadThemeColor = async () => {
      try {
        const colorId = await settingsService.getThemeColor();
        setThemeColorId(colorId);
        setThemeColorState(settingsService.getThemeColorConfig(colorId));
        localStorage.setItem('themeColor', colorId);
      } catch (error) {
        console.error('Failed to load theme color:', error);
      }
    };
    loadThemeColor();
  }, []);

  useEffect(() => {
    const root = window.document.documentElement;
    root.classList.remove('light', 'dark');
    root.classList.add(theme);
    localStorage.setItem('theme', theme);
  }, [theme]);

  useEffect(() => {
    localStorage.setItem('language', language);
  }, [language]);

  useEffect(() => {
    localStorage.setItem('themeColor', themeColorId);
    setThemeColorState(settingsService.getThemeColorConfig(themeColorId));
  }, [themeColorId]);

  const toggleTheme = () => {
    setTheme(prev => prev === 'light' ? 'dark' : 'light');
  };

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
  };

  const setThemeColor = async (colorId: string) => {
    try {
      await settingsService.setThemeColor(colorId);
      setThemeColorId(colorId);
    } catch (error) {
      console.error('Failed to set theme color:', error);
    }
  };

  const t = (key: string): string => {
    return translations[language][key as keyof typeof translations['en']] || key;
  };

  return (
    <ThemeContext.Provider value={{ theme, language, themeColor, toggleTheme, setLanguage, setThemeColor, t }}>
      {children}
    </ThemeContext.Provider>
  );
}

export function useTheme() {
  const context = useContext(ThemeContext);
  if (!context) {
    throw new Error('useTheme must be used within ThemeProvider');
  }
  return context;
}
